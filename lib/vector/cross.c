#include "ft_vector.h"

t_vec3	ft_cross(t_vec3 v1, t_vec3 v2)
{
	t_vec3	v;

	v.x = (v1.y * v2.z) - (v2.y * v1.z);
	v.y = (v1.z * v2.x) - (v2.z * v1.x);
	v.z = (v1.x * v2.y) - (v2.x * v1.y);
	return (v);
}
