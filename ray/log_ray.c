#include "minirt.h"

void	log_ray(t_ray *r)
{
	printf("ray: d = (%f, %f, %f); o = (%f, %f, %f) t = %f\n",
		r->d.x, r->d.y, r->d.z, r->o.x, r->o.y, r->o.z, r->t);
}
